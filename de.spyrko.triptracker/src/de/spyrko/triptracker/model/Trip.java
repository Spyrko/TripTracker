package de.spyrko.triptracker.model;

public class Trip {
	
	private String  h1, 
					h2,
					date;

	public String getH1()
	{
		return h1;
	}

	public void setH1(String h1)
	{
		this.h1 = h1;
	}

	public String getH2()
	{
		return h2;
	}

	public void setH2(String h2)
	{
		this.h2 = h2;
	}

	public String getDate()
	{
		return date;
	}

	public void setDate(String date)
	{
		this.date = date;
	}
	
}
